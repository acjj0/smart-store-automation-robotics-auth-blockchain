package com.cscie97.store.model;

/**
 * Enum to create standard and consistent appliance type info
 */
public enum ApplianceTypesEnum {
    /**
     * Speakers are appliances that talk to customers
     */
    SPEAKER,

    /**
     * Robots are appliances that listen to customers, talk to customers, perform tasks
     */
    ROBOT,

    /**
     * Turnstiles are appliances that listen to customers, talk to customers, open and close turnstile
     */
    TURNSTILE
}
